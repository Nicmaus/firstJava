class Pokemon {
  constructor(name, typ, hp, ap, skill) {
    this.name = name;
    this.typ = typ;
    this.hp = hp;
    this.ap = ap;
    this.skill = skill;
    this.initialHp = hp;
    this.initialAp = ap;
    this.wins = 0;
    this.losses = 0;
  }
  fight(poke2) {
    // console.clear();
    console.log(
      "\x1b[0m",
      `___________________________________________________________`
    );
    console.log("\x1b[33m%s\x1b[0m", `${this.name} vs ${poke2.name}`);
    this.info();
    poke2.info();
    console.log(
      "\x1b[0m",
      `___________________________________________________________`
    );
    while (poke2.hp >= 100 && this.hp >= 100 && this.ap > 0 && poke2.ap > 0) {
      poke2.hp = poke2.hp - this.ap;
      this.ap = this.ap - 50;
      this.hp = this.hp - poke2.ap;
      poke2.ap = poke2.ap - 50;
      console.log(
        `${this.name} attacks ${poke2.name} with ${this.skill} =>\n`,
        `${poke2.name} hp =`,
        poke2.hp,
        `\n`,
        `${this.name} ap =`,
        this.ap
      );
      console.log(
        `${poke2.name} attacks ${this.name} with ${poke2.skill} <=\n`,
        `${this.name} hp =`,
        this.hp,
        `\n`,
        `${poke2.name} ap =`,
        poke2.ap
      );
    }
    if (poke2.hp <= 100) {
      console.log(
        "\x1b[33m%s\x1b[0m",
        `\n ${poke2.name} is defeated!${emoji1} \n ${this.name} has won the fight.${emoji2}`
      );
      poke2.resetHpAp();
      this.resetHpAp();
      this.wins++;
      poke2.losses++;
    }
    if (this.hp <= 100) {
      console.log(
        "\x1b[33m%s\x1b[0m",
        `\n ${this.name} is defeated!${emoji1} \n ${poke2.name} has won the fight. ${emoji2}`
      );
      this.resetHpAp();
      poke2.resetHpAp();
      poke2.wins++;
      this.losses++;
    }
    if (this.ap <= 0 && poke2.ap <= 0) {
      poke2.resetHpAp();
      this.resetHpAp();
      console.log(
        "\x1b[33m%s\x1b[0m",
        `\n ${this.name} and ${poke2.name} don't have any ap left. \n The fight is over. ${emoji5}`
      );
    }
    if (this.ap <= 0) {
      console.log(
        "\x1b[33m%s\x1b[0m",
        `\n ${this.name} can't attack anymore.${emoji1} \n ${poke2.name} has won the fight. ${emoji2}`
      );
      poke2.resetHpAp();
      this.resetHpAp();
      poke2.wins++;
      this.losses++;
    }
    if (poke2.ap <= 0) {
      console.log(
        "\x1b[33m%s\x1b[0m",
        `\n ${poke2.name} can't attack anymore.${emoji1} \n ${this.name} has won the fight. ${emoji2}`
      );
      this.resetHpAp();
      poke2.resetHpAp();
      this.wins++;
      poke2.losses++;
    }
    console.log(
      "\x1b[0m",
      `___________________________________________________________`
    );
  }
  info() {
    console.log(picture(this.name));
    console.log(
      "\x1b[34m%s\x1b[0m",
      `${this.name} is a Pokemon from type ${this.typ}. Has the following attributes: ${this.hp}hp${emoji3} , ${this.ap}ap${emoji4} and Skill: ${this.skill}.`,
      "\x1b[0m"
    );
  }
  resetHpAp() {
    this.hp = this.initialHp;
    this.ap = this.initialAp;
  }
  statistic() {
    // console.clear();
    console.log(picture(this.name));
    console.log(
      "\x1b[37m%s\x1b[1m",
      `${this.name} has ${this.wins} wins and ${this.losses} losses.`
    );
  }
}

